  function searchLunbo(){
    var $Wrap = $('.cont_width'),
        $prev = $('.prevIcon'),
        $next = $('.nextIcon'),
        initN = 0,
        t = '';
        $Wrap.children('.ul2').html($Wrap.children('.ul1').html());
        var $liWrap = $('.cont_width ul li');
        var liSize = ($liWrap.width()+11)*$liWrap.size();
        var pageSize = Math.ceil($liWrap.size() / 3) / 2;
        $Wrap.width(liSize);
        if(pageSize <= 1)return;
        $('.search_ad_cont').hover(function(){
          $prev.fadeIn(300);
          $next.fadeIn(300);
          clearInterval(t);
        },function(){
          $prev.fadeOut(300);
          $next.fadeOut(300);
          setInter();
        });
        $next.bind('click',function(event) {
            initN++;
            if(initN > pageSize){
            initN = 1;
             $Wrap.css('margin-left','0');
          }
            changeFn();
        });
        $prev.bind('click',function(event) {
            initN--;
            if(initN < 0){
              initN = pageSize;
              $Wrap.css('margin-left',-initN * ($liWrap.width()+11) * 3 + "px");
              initN--;
            }
            changeFn();
        });
        function changeFn(){
            $Wrap.stop().animate({marginLeft:-initN * ($liWrap.width()+11) * 3 + "px"}, 400);
        }
        setInter();
        function setInter(){
          clearInterval(t);
          t = setInterval(function(){
              initN++;
              if(initN > pageSize){
              initN = 1;
               $Wrap.css('margin-left','0');
            }
              changeFn();
          },5000);
        }
  }
  
